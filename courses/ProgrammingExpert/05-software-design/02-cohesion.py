
# cohesion - the degree to which things grouped together are similar ...

# funtional cohesion - when a module performs one and only one task

# sequential cohesion - when a module performs a series of actions, one after the other

# communicational cohesion - when a module performs a series of actions related to a data structure

# procedural cohesion - when a module performs a series of actions related to a control structure

# temporal cohesion - when a module performs a series of actions related to a time sequence

# logical cohesion - when a module performs a series of actions related to a logical structure

# layer cohesion - when a module performs a series of actions related to a hierarchy of abstraction

